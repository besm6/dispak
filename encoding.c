/*
 * Fetch GOST-10859 symbol from UTF-8 string.
 * Advance string pointer.
 */
uchar
utf8_to_gost (uchar **p)
{
	int c1, c2, c3;

	c1 = *(*p)++;
	if (! (c1 & 0x80))
		return unicode_to_gost (c1);
	c2 = *(*p)++;
	if (! (c1 & 0x20))
		return unicode_to_gost ((c1 & 0x1f) << 6 | (c2 & 0x3f));
	c3 = *(*p)++;
	return unicode_to_gost ((c1 & 0x0f) << 12 | (c2 & 0x3f) << 6 |
		(c3 & 0x3f));
}

/*
 * Write GOST-10859 symbol to file.
 * Convert to local encoding (UTF-8, KOI8-R, CP-1251, CP-866).
 */
void
gost_putc (uchar ch, FILE *fout)
{
	const ushort *gost_to_unicode = pout_latin ?
		gost_to_unicode_lat : gost_to_unicode_cyr;
	ushort u;

	u = gost_to_unicode [ch];
	if (! u)
		u = '?';
	unicode_putc (u, fout);
}

/*
 * Write GOST-10859 string to file.
 * Convert to local encoding (UTF-8, KOI8-R, CP-1251, CP-866).
 */
void
gost_write (uchar *line, int n, FILE *fout)
{
	const ushort *gost_to_unicode = pout_latin ?
		gost_to_unicode_lat : gost_to_unicode_cyr;
	ushort u;

	while (n-- > 0) {
		u = gost_to_unicode [*line++];
		if (! u)
			u = '?';
		unicode_putc (u, fout);
	}
}

/*
 * Put UTF-8 string to file.
 * Convert to local encoding (UTF-8, KOI8-R, CP-1251, CP-866).
 */
void
utf8_puts (char *line, FILE *fout)
{
	int c1, c2, c3;

	while (*line) {
		c1 = *line++;
		if (! (c1 & 0x80)) {
			unicode_putc (c1, fout);
			continue;
		}
		c2 = *line++;
		if (! (c1 & 0x20)) {
			unicode_putc ((c1 & 0x1f) << 6 | (c2 & 0x3f), fout);
			continue;
		}
		c3 = *line++;
		unicode_putc ((c1 & 0x0f) << 12 | (c2 & 0x3f) << 6 |
			(c3 & 0x3f), fout);
	}
}
